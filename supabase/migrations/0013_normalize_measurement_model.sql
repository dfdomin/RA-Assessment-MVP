CREATE INDEX IF NOT EXISTS idx_module_analysis_evaluation ON public.module_analysis(module_ra_evaluation_id);

CREATE POLICY "module_analysis_insert_teacher" ON public.module_analysis
    FOR INSERT TO authenticated
    WITH CHECK (public.is_evaluation_teacher(module_ra_evaluation_id));

CREATE POLICY "module_analysis_update_teacher" ON public.module_analysis
    FOR UPDATE TO authenticated
    USING (public.is_evaluation_teacher(module_ra_evaluation_id));

