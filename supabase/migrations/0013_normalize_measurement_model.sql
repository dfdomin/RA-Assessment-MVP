-- Migration 0013: Normalize measurement model (PRD v2.4)
-- - module_ra_evaluations: asignación módulo físico × período de captura (RA)
-- - modules: solo módulo físico (ciclo + programa + código + grupo)
-- - module_analysis: alcance por evaluación, no por módulo físico
-- - leader_analysis, action_plans, leader_report_drafts: alcance programa × período

-- =============================================================================
-- 1. module_ra_evaluations
-- =============================================================================
CREATE TABLE public.module_ra_evaluations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    module_id BIGINT NOT NULL REFERENCES public.modules(id) ON DELETE CASCADE,
    period_id BIGINT NOT NULL REFERENCES public.periods(id) ON DELETE CASCADE,
    status VARCHAR(20) NOT NULL DEFAULT 'pending'
        CHECK (status IN ('pending', 'in_progress', 'completed')),
    submitted_at TIMESTAMPTZ,
    UNIQUE (module_id, period_id)
);

CREATE INDEX idx_mre_module ON public.module_ra_evaluations(module_id);
CREATE INDEX idx_mre_period ON public.module_ra_evaluations(period_id);
CREATE INDEX idx_mre_period_status ON public.module_ra_evaluations(period_id, status);

INSERT INTO public.module_ra_evaluations (module_id, period_id, status, submitted_at)
SELECT id, period_id, status, submitted_at
FROM public.modules
WHERE period_id IS NOT NULL;

-- =============================================================================
-- 2. modules → módulo físico (cycle_id, sin period_id/status)
-- =============================================================================
ALTER TABLE public.modules
    ADD COLUMN IF NOT EXISTS cycle_id BIGINT REFERENCES public.measurement_cycles(id);

UPDATE public.modules m
SET cycle_id = p.cycle_id
FROM public.periods p
WHERE p.id = m.period_id AND m.cycle_id IS NULL;

UPDATE public.modules m
SET cycle_id = mc.id
FROM public.measurement_cycles mc
WHERE mc.code = '2025-2' AND m.cycle_id IS NULL;

CREATE TEMP TABLE _module_redirect AS
SELECT
    id AS old_id,
    MIN(id) OVER (
        PARTITION BY cycle_id, COALESCE(program_id, -1), course_code, group_name
    ) AS keep_id
FROM public.modules
WHERE cycle_id IS NOT NULL;

DELETE FROM _module_redirect WHERE old_id = keep_id;

UPDATE public.module_ra_evaluations mre
SET module_id = r.keep_id
FROM _module_redirect r
WHERE mre.module_id = r.old_id
  AND NOT EXISTS (
      SELECT 1 FROM public.module_ra_evaluations existing
      WHERE existing.module_id = r.keep_id AND existing.period_id = mre.period_id
  );

DELETE FROM public.module_ra_evaluations mre
USING _module_redirect r
WHERE mre.module_id = r.old_id;

INSERT INTO public.module_staff (module_id, user_id)
SELECT r.keep_id, ms.user_id
FROM public.module_staff ms
JOIN _module_redirect r ON r.old_id = ms.module_id
ON CONFLICT (module_id, user_id) DO NOTHING;

DELETE FROM public.module_staff ms
USING _module_redirect r
WHERE ms.module_id = r.old_id;

UPDATE public.module_students ms
SET module_id = r.keep_id
FROM _module_redirect r
WHERE ms.module_id = r.old_id
  AND NOT EXISTS (
      SELECT 1 FROM public.module_students existing
      WHERE existing.module_id = r.keep_id AND existing.student_id = ms.student_id
  );

DELETE FROM public.module_students ms
USING _module_redirect r
WHERE ms.module_id = r.old_id;

DELETE FROM public.modules m
USING _module_redirect r
WHERE m.id = r.old_id;

ALTER TABLE public.modules DROP COLUMN IF EXISTS period_id;
ALTER TABLE public.modules DROP COLUMN IF EXISTS status;
ALTER TABLE public.modules DROP COLUMN IF EXISTS submitted_at;

DROP INDEX IF EXISTS idx_modules_period;
DROP INDEX IF EXISTS idx_modules_period_status;

CREATE UNIQUE INDEX idx_modules_physical_unique
    ON public.modules (cycle_id, COALESCE(program_id, -1), course_code, group_name);

CREATE INDEX IF NOT EXISTS idx_modules_cycle ON public.modules(cycle_id);

ALTER TABLE public.modules
    ALTER COLUMN cycle_id SET NOT NULL;

