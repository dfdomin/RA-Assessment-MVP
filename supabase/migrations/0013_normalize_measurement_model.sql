CREATE TEMP TABLE _period_program AS
SELECT DISTINCT ON (mre.period_id)
    mre.period_id,
    m.program_id
FROM public.module_ra_evaluations mre
JOIN public.modules m ON m.id = mre.module_id
WHERE m.program_id IS NOT NULL
ORDER BY mre.period_id, m.program_id;

