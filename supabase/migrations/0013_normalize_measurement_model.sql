-- Policies must be dropped before removing module_id (they reference that column).
CREATE OR REPLACE FUNCTION public.is_evaluation_teacher(p_evaluation_id bigint)
RETURNS boolean AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1
        FROM public.module_ra_evaluations mre
        JOIN public.module_staff mst ON mst.module_id = mre.module_id
        WHERE mre.id = p_evaluation_id AND mst.user_id = auth.uid()
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

GRANT EXECUTE ON FUNCTION public.is_evaluation_teacher(bigint) TO authenticated;

DROP POLICY IF EXISTS "module_analysis_insert_teacher" ON public.module_analysis;
