CREATE TABLE public.leader_report_drafts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_id BIGINT NOT NULL REFERENCES public.periods(id) ON DELETE CASCADE,
    program_id BIGINT NOT NULL REFERENCES public.programs(id),
    perf_indicator_id BIGINT NOT NULL REFERENCES public.perf_indicators(id),
    conclusion_text TEXT NOT NULL DEFAULT '' CHECK (char_length(conclusion_text) <= 3000),
    updated_by UUID NOT NULL REFERENCES public.users(id),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (period_id, program_id, perf_indicator_id)
);

CREATE INDEX idx_leader_report_drafts_period ON public.leader_report_drafts(period_id);
CREATE INDEX idx_leader_report_drafts_program ON public.leader_report_drafts(program_id);

INSERT INTO public.leader_report_drafts (
    period_id, program_id, perf_indicator_id, conclusion_text, updated_by, updated_at
)
SELECT
    l.period_id,
    pp.program_id,
    (kv.key)::bigint,
    kv.value,
    l.updated_by,
    l.last_updated
FROM public.leader_report_drafts_legacy l
JOIN _period_program pp ON pp.period_id = l.period_id
CROSS JOIN LATERAL jsonb_each_text(l.pi_conclusions) AS kv(key, value)
WHERE kv.key ~ '^\d+$';

DROP TABLE public.leader_report_drafts_legacy;

