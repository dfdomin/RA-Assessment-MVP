CREATE OR REPLACE FUNCTION public.is_program_consolidator(p_program_id bigint, p_period_id bigint)
RETURNS boolean AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1
        FROM public.ra_consolidator_assignments rca
        JOIN public.periods p ON p.id = p_period_id
        WHERE rca.program_id = p_program_id
          AND rca.student_outcome_id = p.student_outcome_id
          AND rca.consolidator_user_id = auth.uid()
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

GRANT EXECUTE ON FUNCTION public.is_program_consolidator(bigint, bigint) TO authenticated;

